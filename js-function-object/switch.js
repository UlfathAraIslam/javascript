// if else work for one by one step to complete
const color = 'yellow';
if(color === 'green'){
    console.log('You are a green friend');
    
}

else if (color === 'blue'){
    console.log('blue friend');

}
else if (color === 'red'){
    console.log('red friend');

}
else if (color === 'white'){
    console.log('blue friend');

}
else if (color === 'yellow'){
    console.log('blue friend');

}
else {
    console.log('black black friend');

}

// switch
switch(color){
    case 'green' :
        console.log('green friend');
        break;
    case 'blue' :
        console.log('blue friend');
        break;
    case 'white' :
        console.log('white friend');
        break;
    case 'red' :
        console.log('red friend');
        break;
    case 'yellow' :
        console.log('yellow friend');
        break;
    default:
        console.log('kala kala friend');

}

