
// add an element to an array at the last position
var numbers = [1, 2, 3, 4,];
numbers.push(5);
console.log(numbers);

// remove last element from array
var numbers = [1, 2, 3, 4, 5];
numbers.pop();
console.log(numbers);

//  add an element at the first position of an array
var fruits = ["banana", "mango", "apple"];
fruits.unshift("lemon");
console.log(fruits);

// remove an element at the first position of an array
var toys = ["ball", "doll", "bat"];
toys.shift();
console.log(toys);